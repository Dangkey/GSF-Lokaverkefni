CREATE TABLE Membership(
    MEM_NUM CHAR(3) Primary Key,
    MEM_FNAME VARCHAR(10),
    MEM_LNAME VARCHAR(10),
    MEM_STREET VARCHAR(30),
    MEM_CITY VARCHAR(15),
    MEM_STATE VARCHAR(2),
    MEM_ZIP CHAR(5),
    MEM_BALANCE CHAR(2)
);
	
CREATE TABLE RENTAL(
    RENT_NUM Char(4) Primary Key,
    RENT_DATE CHAR(9),
    MEM_NUM CHAR(3),
    FOREIGN KEY (MEM_NUM) REFERENCES membership(MEM_NUM)
);
	
	
CREATE TABLE PRICE(
    PRICE_CODE INT AUTO_INCREMENT PRIMARY KEY,
    PRICE_DESCRIPTION VARCHAR(20),
    PRICE_RENTFEE DECIMAL(2,1),
    PRICE_DAILYLATEFEE DECIMAL(2,1)
);
	
CREATE TABLE MOVIE(
	MOVIE_NUM CHAR(4) PRIMARY KEY,
	MOVIE_NAME VARCHAR(40),
	MOVIE_YEAR YEAR,
	MOVIE_COST DECIMAL(4,2),
	MOVIE_GENRE VARCHAR(10),
	PRICE_CODE INT,
	FOREIGN KEY (PRICE_CODE) REFERENCES PRICE(PRICE_CODE)
);
	
CREATE TABLE VIDEO(
    VID_NUM CHAR(5) PRIMARY KEY,
    VID_INDATE CHAR(9),
    MOVIE_NUM CHAR(4),
   	FOREIGN KEY (MOVIE_NUM) REFERENCES MOVIE(MOVIE_NUM)
);
	
	
CREATE TABLE DETAILRENTAL(
    RENT_NUM CHAR(4),
    VID_NUM CHAR(5),
    DETAIL_FEE DECIMAL(2,1),
    DETAIL_DUEDATE CHAR(9),
    DETAIL_RETURNDATE CHAR(9),
    DETAIL_DAILYLATEFEE DECIMAL(2,1),
    PRIMARY KEY(RENT_NUM,VID_NUM),
    FOREIGN KEY (RENT_NUM) REFERENCES RENTAL(RENT_NUM),
    FOREIGN KEY (VID_NUM) REFERENCES VIDEO(VID_NUM)
);